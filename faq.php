      <div class="jeptis_answer">Nogle ejere tilbyder leveringsservice hvor produktet eller servicen leveres på din adresse:

            Det er udlejer der selv sætter prisen for leveringsservice. 

            Prisen er inddelt i to niveauer. Først fastsætter man en zone, som man bringer ud til for en fast pris. 
            Derefter sætter ejer den faste pris for denne startzone. Efterfølgende oplyser ejeren, 
